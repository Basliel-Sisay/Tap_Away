def text_width(text, scale=1):
    """Calculate the width of text in pixels."""
    if not text:
        return 0
    width = 0
    for ch in text.upper():
        glyph = _GLYPHS.get(ch, _GLYPHS.get(" "))
        if glyph:
            width += (len(glyph[0]) + 1) * scale
    return int(width)


def draw_text(ui, x, y, text, color, scale=2):
    """Render text on screen using clean sans-serif glyphs."""
    if not text:
        return
    
    shadow = (
        max(0.0, color[0] * 0.25),
        max(0.0, color[1] * 0.25),
        max(0.0, color[2] * 0.25),
    )

    cursor = float(x)
    for ch in text.upper():
        glyph = _GLYPHS.get(ch, _GLYPHS.get(" "))
        if not glyph:
            continue
        
        glyph_width = len(glyph[0])
        
        for row_idx, row in enumerate(glyph):
            for col_idx, bit in enumerate(row):
                if bit == "1":
                    px = cursor + col_idx * scale
                    py = y + row_idx * scale
                    
                    # Draw shadow with slight offset
                    ui.draw_rect(px + 1, py + 1, scale, scale, shadow, alpha=0.35, radius=scale * 0.25)
                    # Draw main character
                    ui.draw_rect(px, py, scale, scale, color, alpha=0.98, radius=scale * 0.25)
        
        cursor += (glyph_width + 1) * scale
